// 🎯 HASHTAG MATCHING SCORE SYSTEM
export const calculateHashtagMatchScore = (userTags: string[], partnerTags: string[]): number => {
  if (!userTags.length || !partnerTags.length) return 0;
  
  const matchingTags = userTags.filter(tag => partnerTags.includes(tag));
  const matchPercentage = (matchingTags.length / Math.max(userTags.length, partnerTags.length)) * 100;
  
  return Math.round(matchPercentage);
};

// 🏆 GET MATCHING TAGS
export const getMatchingTags = (userTags: string[], partnerTags: string[]): string[] => {
  return userTags.filter(tag => partnerTags.includes(tag));
};

// 📊 GET ALL TAGS (Flat list)
export const getAllHashtags = (): Array<{ tag: string; emoji: string; description: string; category: string }> => {
  const allTags: Array<{ tag: string; emoji: string; description: string; category: string }> = [];
  
  Object.entries(HABESHLIVE_HASHTAGS).forEach(([category, tags]) => {
    tags.forEach(tagInfo => {
      allTags.push({
        ...tagInfo,
        category
      });
    });
  });
  
  return allTags;
};

// 🔍 SEARCH HASHTAGS
export const searchHashtags = (query: string): Array<{ tag: string; emoji: string; description: string; category: string }> => {
  const allTags = getAllHashtags();
  const lowerQuery = query.toLowerCase();
  
  return allTags.filter(tagInfo => 
    tagInfo.tag.toLowerCase().includes(lowerQuery) ||
    tagInfo.description.toLowerCase().includes(lowerQuery)
  );
};

